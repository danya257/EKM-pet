class Command(BaseCommand):
    help = 'Создаёт 5 тематических статей о здоровье питомцев'

    def add_arguments(self, parser):
        parser.add_argument(
            '--force',
            action='store_true',
            help='Перезаписать статьи если они уже существуют',
        )

    def handle(self, *args, **options):
        force = options['force']

        # Берём первого суперпользователя как автора
        author = User.objects.filter(is_superuser=True).first()
        if not author:
            author = User.objects.filter(is_staff=True).first()

        created = 0
        skipped = 0

        for data in ARTICLES:
            exists = Article.objects.filter(slug=data['slug']).exists()
            if exists and not force:
                self.stdout.write(f'  Пропущено (уже есть): {data["title"]}')
                skipped += 1
                continue

            Article.objects.update_or_create(
                slug=data['slug'],
                defaults={
                    'title':        data['title'],
                    'content':      data['content'],
                    'author':       author,
                    'is_published': True,
                }
            )
            self.stdout.write(self.style.SUCCESS(f'  ✓ {data["title"]}'))
            created += 1

        self.stdout.write(
            self.style.SUCCESS(
                f'\nГотово: создано {created}, пропущено {skipped} статей.'
            )
        )
