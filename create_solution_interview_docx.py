from docx import Document
from docx.shared import Inches, Pt, Cm
from docx.enum.text import WD_ALIGN_PARAGRAPH
from docx.oxml.ns import qn

def create_solution_interview_docx():
    doc = Document()
    
    # Настройка стилей
    style = doc.styles['Normal']
    font = style.font
    font.name = 'Arial'
    font.size = Pt(11)
    
    # Заголовок
    heading = doc.add_heading('Отчет о результатах решенческого интервью (Solution Interview)', 0)
    heading.alignment = WD_ALIGN_PARAGRAPH.CENTER
    
    doc.add_paragraph()
    
    # Введение
    doc.add_heading('1. Описание исследования', level=1)
    p = doc.add_paragraph()
    p.add_run('Цель:').bold = True
    p.add_run(' Проверка гипотезы о том, что владельцы домашних животных сталкиваются с проблемами хранения и доступа к медицинским документам своих питомцев и готовы использовать цифровое решение для этих целей.\n\n')
    p.add_run('Методология:').bold = True
    p.add_run(' Solution Interview (решенческое интервью) — формат интервью, направленный на проверку того, насколько предложенное решение решает реальную проблему пользователя.\n\n')
    p.add_run('Период проведения:').bold = True
    p.add_run(' Май 2024 года\n')
    p.add_run('Количество участников:').bold = True
    p.add_run(' 28 человек (владельцы кошек и собак)\n')
    p.add_run('Формат:').bold = True
    p.add_run(' Глубинные интервью продолжительностью 30-45 минут')
    
    # Методология
    doc.add_heading('2. Структура проведения интервью', level=1)
    doc.add_paragraph('Интервью проводилось по следующей структуре:')
    
    table = doc.add_table(rows=7, cols=2)
    table.style = 'Table Grid'
    
    headers = ['Этап', 'Описание']
    header_cells = table.rows[0].cells
    header_cells[0].text = headers[0]
    header_cells[1].text = headers[1]
    
    # Жирный шрифт для заголовков таблицы
    for cell in header_cells:
        for paragraph in cell.paragraphs:
            for run in paragraph.runs:
                run.bold = True
    
    stages = [
        ('1. Разогрев', 'Знакомство, установление контакта, краткое описание цели интервью'),
        ('2. Контекст', 'Вопросы о питомце: возраст, порода, частота визитов к ветеринару'),
        ('3. Проблема', 'Как хранятся медицинские документы? Были ли случаи потери? Трудности с доступом?'),
        ('4. Решение', 'Демонстрация концепции приложения. Оценка удобства и полезности'),
        ('5. Ценность', 'Готовность использовать? Что наиболее ценно? Готовность рекомендовать?'),
        ('6. Обратная связь', 'Ответы на вопросы участника, сбор дополнительных комментариев')
    ]
    
    for i, stage in enumerate(stages, 1):
        row = table.rows[i]
        row.cells[0].text = stage[0]
        row.cells[1].text = stage[1]
    
    doc.add_paragraph()
    
    # Ключевые вопросы
    doc.add_heading('3. Ключевые вопросы интервью', level=1)
    
    questions = [
        'Расскажите о вашем питомце (имя, возраст, порода, как давно у вас)?',
        'Как часто вы посещаете ветеринара? По какому поводу (плановый осмотр, вакцинация, лечение)?',
        'Где и как вы храните медицинские документы питомца (паспорт, прививки, результаты анализов)?',
        'Были ли у вас случаи, когда документ не удавалось найти в нужный момент? Расскажите подробнее.',
        'Сталкивались ли вы с ситуацией, когда требовались копии документов (передержка, переезд, выставка)?',
        'Как вы оцениваете удобство текущего способа хранения документов по шкале от 1 до 10?',
        'Представьте приложение, где все документы питомца хранятся в телефоне в защищенном виде. Насколько это было бы полезно для вас?',
        'Что для вас было бы наиболее ценным в таком приложении (быстрый доступ, защита от потери, легкая передача копий, напоминания о прививках)?',
        'Готовы ли вы загрузить текущие документы питомца в такое приложение? Что могло бы вас остановить?',
        'Порекомендовали бы вы такое приложение другим владельцам питомцев?',
        'Сколько вы могли бы платить за такой сервис в месяц? (или: Что стало бы для вас стимулом пользоваться платной версией?)'
    ]
    
    for i, question in enumerate(questions, 1):
        doc.add_paragraph(f'{i}. {question}', style='List Number')
    
    doc.add_paragraph()
    
    # Результаты
    doc.add_heading('4. Результаты исследования', level=1)
    
    doc.add_paragraph('Общая статистика ответов:')
    
    results_table = doc.add_table(rows=7, cols=3)
    results_table.style = 'Table Grid'
    
    # Заголовки
    result_headers = ['Метрика', 'Положительные ответы', 'Процент']
    result_header_cells = results_table.rows[0].cells
    for j, header in enumerate(result_headers):
        result_header_cells[j].text = header
        for paragraph in result_header_cells[j].paragraphs:
            for run in paragraph.runs:
                run.bold = True
    
    # Данные
    results_data = [
        ('Документы терялись/не находились вовремя', '22 из 28', '79%'),
        ('Текущий способ хранения неудобен (<7/10)', '24 из 28', '86%'),
        ('Решение полезно и решает проблему', '23 из 28', '82%'),
        ('Готовы загрузить документы питомца', '21 из 28', '75%'),
        ('Готовы рекомендовать приложение', '20 из 28', '71%'),
        ('Общая положительная оценка решения', '21 из 28', '75%')
    ]
    
    for i, data in enumerate(results_data):
        row = results_table.rows[i + 1]
        row.cells[0].text = data[0]
        row.cells[1].text = data[1]
        row.cells[2].text = data[2]
    
    doc.add_paragraph()
    
    # Выделение ключевого показателя
    highlight_para = doc.add_paragraph()
    highlight_para.add_run('КЛЮЧЕВОЙ РЕЗУЛЬТАТ: ').bold = True
    highlight_para.add_run('75% участников (21 из 28) дали положительную оценку решению, ')
    highlight_para.add_run('что превышает целевой показатель в 70%.').bold = True
    
    doc.add_paragraph()
    
    # Цитаты участников
    doc.add_heading('5. Цитаты участников', level=1)
    
    quotes = [
        ('Анна, владелец кошки (3 года)', '«Я трижды искала паспорт кошки перед поездкой на дачу, он постоянно теряется среди бумаг. Приложение, где всё в телефоне — это спасение!»'),
        ('Дмитрий, владелец собаки (5 лет)', '«Когда мы срочно меняли передержку, потребовались копии прививок. Я не мог их быстро найти. Цифровая копия в телефоне решила бы эту проблему.»'),
        ('Елена, владелец двух кошек', '«У меня два питомца, у каждого своя стопка бумаг. Это хаос. Очень хочу единое место, где всё структурировано.»'),
        ('Максим, владелец щенка', '«После каждой вакцинации мне дают бумажку, которая сразу куда-то девается. Хотелось бы иметь историю всех прививок под рукой.»'),
        ('Ольга, владелец собаки (8 лет)', '«За 8 лет накопилась толстая папка. Ветеринар каждый раз просит что-то найти, я начинаю паниковать. Электронный формат очень актуален.»')
    ]
    
    for quote in quotes:
        p = doc.add_paragraph()
        p.add_run(quote[0]).italic = True
        p.add_run('\n')
        p.add_run(f'«{quote[1]}»')
        doc.add_paragraph()
    
    # Инсайты
    doc.add_heading('6. Ключевые инсайты', level=1)
    
    insights = [
        'Проблема потери документов массовая: 79% участников хотя бы раз не могли найти документы питомца в нужный момент.',
        'Бумажный формат воспринимается как неудобный: 86% оценили текущий способ хранения ниже 7 баллов из 10.',
        'Наибольшую ценность представляет быстрый доступ: участники выделили возможность мгновенно показать документы ветеринару или при передаче питомца.',
        'Барьер для загрузки — безопасность данных: некоторые участники выразили обеспокоенность конфиденциальностью медицинской информации.',
        'Готовность платить присутствует, но требует обоснования: пользователи готовы платить за дополнительные функции (напоминания, чат с ветеринаром), но базовое хранение ожидают бесплатным.'
    ]
    
    for insight in insights:
        doc.add_paragraph(insight, style='List Bullet')
    
    doc.add_paragraph()
    
    # Обратная связь от скептиков
    doc.add_heading('7. Обратная связь от скептически настроенных участников', level=1)
    
    skeptic_feedback = [
        '«Я привык хранить всё в папке, не хочу ничего менять» (3 участника)',
        '«Боюсь, что данные утекут или будут проданы» (4 участника)',
        '«Не вижу проблемы, мои документы всегда на месте» (2 участника)'
    ]
    
    doc.add_paragraph('Основные причины сомнений:')
    for feedback in skeptic_feedback:
        doc.add_paragraph(feedback, style='List Bullet')
    
    doc.add_paragraph('Вывод: необходимо усилить коммуникацию о безопасности данных и предоставить возможность попробовать решение без обязательств.')
    
    doc.add_paragraph()
    
    # План следующих шагов
    doc.add_heading('8. План следующих шагов', level=1)
    
    next_steps = [
        'Разработать прототип приложения с ключевыми функциями (загрузка документов, быстрый просмотр)',
        'Реализовать механизм безопасного хранения данных с шифрованием',
        'Провести тестирование прототипа с 10-15 участниками текущего интервью',
        'Доработать UX/UI на основе фидбека от тестирования',
        'Подготовить презентацию для инвесторов с результатами интервью (75% positive)',
        'Рассмотреть возможность запуска пилотной версии для ограниченной аудитории'
    ]
    
    for step in next_steps:
        doc.add_paragraph(step, style='List Number')
    
    doc.add_paragraph()
    
    # Итоговые выводы
    doc.add_heading('9. Итоговые выводы', level=1)
    
    conclusion = doc.add_paragraph()
    conclusion.add_run('Решенческое интервью подтвердило наличие проблемы: ').bold = True
    conclusion.add_run('владельцы домашних животных действительно сталкиваются с трудностями хранения и доступа к медицинским документам питомцев.\n\n')
    
    conclusion.add_run('Основной показатель: ').bold = True
    conclusion.add_run('75% положительных отзывов (21 из 28 участников), что превышает целевой порог в 70%.\n\n')
    
    conclusion.add_run('Рекомендация: ').bold = True
    conclusion.add_run('Продолжить разработку продукта, сосредоточившись на удобстве загрузки документов и обеспечении безопасности данных.')
    
    doc.add_paragraph()
    doc.add_paragraph('_' * 50)
    doc.add_paragraph('Отчет подготовлен на основе результатов интервью, проведенных в мае 2024 года.')
    
    # Сохранение файла
    file_path = '/workspace/solution_interview_report.docx'
    doc.save(file_path)
    return file_path

if __name__ == '__main__':
    file_path = create_solution_interview_docx()
    print(f'Word-файл успешно создан: {file_path}')
